import Foundation

class MathFunc{
    
    func sum(x:Int,y:Int) -> Int{
        return x + y
    }
    func multi(x:Int,y:Int) -> Int{
        return x * y
    }
    func divide(x:Int,y:Int) -> Int{
        return x / y
    }
}
